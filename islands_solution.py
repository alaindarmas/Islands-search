from collections import deque

def islandsBFS(grid):
    if not grid:
        return 0

    # Get the number of rows and columns in the grid
    numberOfRows = len(grid)
    numberOfColumns = len(grid[0])

    # Initialize a 2D list to keep track of visited position
    visitedPositions = []
    for row in range(numberOfRows):
        rowList = []
        for _ in range(numberOfColumns):
            rowList.append(False)
        visitedPositions.append(rowList)

    # Start with zero islands
    numberOfIslands = 0

    # Possible movements are right, down, left, up
    directions = [(0, 1), (1, 0), (0, -1), (-1, 0)]

    # Go through each position in the grid
    for row in range(numberOfRows):
        for column in range(numberOfColumns):
            # If the position is land and has not been visited yet
            if grid[row][column] == 1 and not visitedPositions[row][column]:
                # Use a queue to keep track of positions to visit
                queue = deque()
                queue.append((row, column))

                # Mark the starting position as visited
                visitedPositions[row][column] = True

                # Continue until there are no more positions to visit
                while queue:
                    currentRow, currentColumn = queue.popleft()

                    # Check all four neighboring positions
                    for deltaRow, deltaColumn in directions:
                        newRow = currentRow + deltaRow
                        newColumn = currentColumn + deltaColumn

                        # If the neighbor is within the grid
                        if 0 <= newRow < numberOfRows and 0 <= newColumn < numberOfColumns:
                            # If the neighbor hasn't been visited and is land
                            if not visitedPositions[newRow][newColumn] and grid[newRow][newColumn] == 1:
                                # Mark neighbor as visited
                                visitedPositions[newRow][newColumn] = True
                                # Add neighbor to the queue to explore its neighbors
                                queue.append((newRow, newColumn))

                # Increase the island count after exploring an entire island
                numberOfIslands += 1

    return numberOfIslands



